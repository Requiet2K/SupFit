package com.project.supplement.security.auth;

import com.project.supplement.entity.Cart;
import com.project.supplement.entity.User;
import com.project.supplement.helper.NameConverter;
import com.project.supplement.repository.UserRepository;
import com.project.supplement.security.Role;
import com.project.supplement.security.config.JwtService;
import lombok.RequiredArgsConstructor;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.security.authentication.AuthenticationManager;
import org.springframework.security.authentication.UsernamePasswordAuthenticationToken;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.stereotype.Service;

@Service
@RequiredArgsConstructor
public class AuthService {

    private final UserRepository userRepository;
    private final PasswordEncoder passwordEncoder;
    private final JwtService jwtService;
    private final AuthenticationManager authenticationManager;
    private final NameConverter nameConverter;

    @Value("${jwt.expiration-time}")
    private long defaultExpirationTime;

    public AuthResponse register(RegisterRequest request) {
        request.setFirstName(nameConverter.nameConverter(request.getFirstName().toLowerCase()));
        request.setLastName(nameConverter.nameConverter(request.getLastName().toLowerCase()));
        var user = User.builder()
                .firstName(request.getFirstName())
                .lastName(request.getLastName())
                .email(request.getEmail())
                .password(passwordEncoder.encode(request.getPassword()))
                .tokenValidation(defaultExpirationTime)
                .role(Role.USER)
                .build();
        Cart cart = new Cart();
        user.setCart(cart);
        cart.setUser(user);
        userRepository.save(user);
        var jwtToken = jwtService.generateToken(user);
        return AuthResponse.builder()
                .token(jwtToken)
                .build();
    }

    public AuthResponse authenticate(LoginRequest request) {
        authenticationManager.authenticate(
                new UsernamePasswordAuthenticationToken(
                        request.getEmail(),
                        request.getPassword()
                )
        );
        var user = userRepository.findByEmail(request.getEmail())
                .orElseThrow();
        var jwtToken = jwtService.generateToken(user);
        return AuthResponse.builder()
                .token(jwtToken)
                .build();
    }
}
